CREATE TABLE CURRENCY_CONVERSION_TRANSACTION(
                           TRANSACTION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
                           TRANSACTION_DATE TIMESTAMP,
                           TRANSACTION_DAY VARCHAR2(10),
                           SOURCE_CURRENCY VARCHAR2(3),
                           TARGET_CURRENCY VARCHAR2(3),
                           SOURCE_AMOUNT NUMBER(10, 2),
                           TARGET_AMOUNT NUMBER(10, 2),
                           CONVERSION_RATE NUMBER(10, 2),
                           PRIMARY KEY(TRANSACTION_ID)
);
CREATE INDEX INDEX1 ON CURRENCY_CONVERSION_TRANSACTION(TRANSACTION_DAY);
